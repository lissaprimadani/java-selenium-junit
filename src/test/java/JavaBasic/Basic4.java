package JavaBasic;

public class Basic4 {
    public static void main(String[] args) {

        //Contoh membuat type data boolean -> true & false//
        boolean Benar = true;
        boolean Salah = false;
        System.out.println(Benar);
        System.out.println(Salah);



        //Contoh type data String//
        String FirstName = "Lissa";
        String LastName = "Christin";
        String Fullname = FirstName + " " + LastName;

        System.out.println(FirstName);
        System.out.println(LastName);
        System.out.println(Fullname);




        //Kalau nama terdiri dari dua kata//
        String name;
        name = "Jennie Jeniferlyn";
        //kalau Stringt hanya satu kata//
        String alamat = "Bali";
        int umur = 30;
        System.out.println(name);
        System.out.println(umur);
        System.out.println(alamat);


        //Contoh type data var//
        var ContohVar = "Lissa Dari Bali";
        System.out.println(ContohVar);


    }
}
